"""
scrape_catalog.py
=================
Professional full-catalog scraper for texnomart.uz

Architecture:
  CatalogScraper    -- orchestrates categories -> pages -> products
  BatchRepository   -- wraps JsonTemplateProductRepository with slug-cache
                       and handles empty / corrupt JSON files

Features:
  - Paginates every category (MAX_PAGES pages, ~20 products / page)
  - Rich progress bars + live stats table
  - Idempotent: skips slugs already in products.json
  - Batch-saves after every page to survive crashes
  - Configurable polite crawling delay
  - ASCII-safe output (Windows cp1252 compatible)
  - Final summary report

Usage:
  python scrape_catalog.py
  python scrape_catalog.py --max-pages 5 --delay 1.0
"""
